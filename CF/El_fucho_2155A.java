import java.util.*;

public class El_Fucho {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            int n = sc.nextInt();
            System.out.println(2 * n - 2);
        }
        sc.close();
    }
}
